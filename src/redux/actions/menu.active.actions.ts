      export const setActiveMenu = (menuId: string): SetActiveMenuAction => ({
            type: SET_ACTIVE_MENU,
            payload: menuId,
      });

      export const clearActiveMenu = (): ClearActiveMenuAction => ({
            type: CLEAR_ACTIVE_MENU,
      });